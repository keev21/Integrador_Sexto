function init() {
  $("#form_palabras").on("submit", function (e) {
    guardaryeditar(e);
  });
}
// ********************************************************************************************************************************************************************************************************************
// ********************************************************************************************************************************************************************************************************************
$().ready(() => {
  //detecta carga de la pagina
  todos_controlador();
});
// **********************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************
var todos_controlador = () => {
  var todos = new Palabras_Model("","","todos");
  todos.todos();
};
// **********************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************
var guardaryeditar = (e) => {
  e.preventDefault();
  var formData = new FormData($("#form_palabras")[0]);
 
  var id = document.getElementById("id").value
  console.log(id);
  if(id > 0){
    var palabras = new Palabras_Model("","",formData,"editar");
    palabras.editar();
  }else{
    var palabras = new Palabras_Model("",formData,"insertar");
    palabras.insertar();  
  }
};
// **********************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************
var editar = (id) => {
  var uno = new Palabras_Model(id,"","uno");
  uno.uno();
};
// **********************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************

var eliminar=(id)=>{
    var eliminar = new Palabras_Model(id,"","eliminar");
    eliminar.eliminar();
}
// **********************************************************************************************************************************************************************************************************************
// **********************************************************************************************************************************************************************************************************************
init();
